      <div id="27">
        <h3>Файл. Файлові системи з погляду користувача. Призначення.</h3>
        <p>
          Файл — це набір даних, до якого можна звертатися за іменем. Файли
          організовані у файлові системи. З погляду користувача файл є
          мінімальним обсягом даних файлової системи, з яким можна працювати
          незалежно. Наприклад, користувач не може зберегти дані на зовнішньому
          носії, не звернувшись при цьому до файлу. Розглянемо особливості
          використання файлів.
        </p>
        <ul>
          <li>
            Файли є найпоширенішим засобом зберігання інформації в
            енергонезалежній пам'яті. Така пам'ять надійніша, й інформація на
            ній може зберігатися так довго, як це необхідно. Зазначимо, що
            більшість збоїв у роботі ОС не руйнує інформації, що зберігається у
            файлах на диску. Для забезпечення збереження даних підвищеної
            цінності вживають додаткових заходів (гаряче резервування, резервне
            копіювання тощо).
          </li>
          <li>
            Файли забезпечують найпростіший варіант спільного використання даних
            різними застосуваннями. Це пов'язано з тим, що файли відокремлені
            від програм, які їх використовують: будь-яка програма, якій відоме
            ім'я файла, може отримати доступ до його вмісту. Якщо одна програма
            запише у файл, а інша його потім прочитає, то ці дві програми
            виконають обмін даними.
          </li>
        </ul>
        <p>Поняття файлової системи</p>
        <p>
          Файлова система - це підсистема ОС, що підтримує організований набір
          файлів, здебільшого у конкретній ділянці дискового простору (логічну
          структуру); низькорівневі структури даних, використовувані для
          організації цього простору у вигляді набору файлів (фізичну
          структуру); програмний інтерфейс файлової системи (набір системних
          викликів, що реалізують операції над файлами).
        </p>
        <p>
          Файлова система надає прикладним програмам абстракцію файла. Прикладні
          програми не мають інформації про те, як організовані дані файла, як
          знаходять відповідність між ім'ям файла і його даними, як пересилають
          дані із диска у пам'ять тощо - усі ці операції забезпечує файлова
          система.
        </p>
        <p>
          Важливо зазначити, що файлові системи можуть надавати інтерфейс
          доступу не тільки до диска, але й до інших пристроїв. Є навіть файлові
          системи, які не зберігають інформацію, а генерують її динамічно за
          запитом. Втім, для прикладних програм усі такі системи мають однаковий
          вигляд.
        </p>
        <p>
          До головних задач файлової системи можна віднести: організацію її
          логічної структури та її відображення на фізичну організацію
          розміщення даних на диску; підтримку програмного інтерфейсу файлової
          системи; забезпечення стійкості проти збоїв; забезпечення розподілу
          файлових ресурсів за умов багатозадачності та захисту даних від
          несанкціонованого доступу.
        </p>
      </div>
