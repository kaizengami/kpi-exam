      <div id="25">
        <h3>
          Основні поняття бази даних. Типи даних, що зберігаються в базі даних.
          Проектування бази даних і створення структури бази даних. Основні
          команди для роботи з таблицями.
        </h3>
        <p>
          База даних (англ. database) – сукупність даних, організованих
          відповідно до концепції, яка описує характеристику цих даних і
          взаємозв'язки між їх елементами; ця сукупність підтримує щонайменше
          одну з областей застосування (за стандартом ISO/IEC 2382:2015[1]). В
          загальному випадку база даних містить схеми, таблиці, подання,
          збережені процедури та інші об'єкти. Дані у базі організовують
          відповідно до моделі організації даних. Таким чином, сучасна база
          даних, крім саме даних, містить їх опис та може містити засоби для їх
          обробки.
        </p>
        <p>
          В загальному випадку базою даних можна вважати будь-який впорядкований
          набір даних. Наприклад, паперову картотеку з формулярами про
          працівників підприємства у відділі кадрів. Але дана стаття зосереджена
          на використанні баз даних в інформаційних системах. На даний час
          застосунки для роботи з базами даних є одними з найпоширеніших
          прикладних програм.
        </p>
        <p>Проектування бази даних.</p>
        <p>
          Перш ніж створити електронну базу даних, необхідно детально розробити
          її структуру. Цей процес включає наступні етапи:
        </p>
        <ul>
          <li>Визначення мети створення бази даних.</li>
          <li>Визначення таблиць, які повинна містити база даних.</li>
          <li>
            Визначення для кожної таблиці списку полів і призначення імен
            кожному полю.
          </li>
          <li>Визначення полів з унікальними значеннями у кожному записі.</li>
          <li>Визначення зв’язків між таблицями.</li>
          <li>Удосконалення структури бази даних.</li>
          <li>Введення даних і створення інших об’єктів бази даних.</li>
          <li>Використання засобів аналізу бази даних.</li>
        </ul>
        <p>
          База даних повинна задовольняти вимогам тих користувачів, які будуть з
          нею працювати. Тому на першому етапі необхідно визначити, які функції
          буде виконувати база даних, яка інформація буде міститись в її
          таблицях. Визначити, яку результативну інформацію необхідно отримувати
          у звітах. Після визначення мети створення бази даних, стане
          зрозумілим, які дані повинні міститися в її таблицях. Добре продумана
          структура таблиць є запорукою ефективної роботи з даними. Необхідно
          детально продумати структуру таблиць, враховуючи наступні вимоги:
        </p>
        <ul>
          <li>Інформація в таблиці не повинна повторюватись.</li>
          <li>Таблиці не повинні містити одну й ту ж інформацію.</li>
          <li>
            Кожна таблиця повинна містити інформацію лише на визначену тему. Щоб
            не відбувалось дублювання даних, необхідно розподілити різнорідні
            дані по окремих таблицях. Для кожного типу відомостей необхідно
            створювати окреме поле. Якщо таблиці містять значну кількість даних,
            можна використати майстер аналізу таблиць (меню
            Сервис/Анализ/Таблица), який допоможе розділити їх на зв’язані
            таблиці, тим самим підвищивши ефективність збереження даних.
          </li>
          <li>
            Для кожного поля таблиці необхідно задати тип даних і вибрати
            найменший розмір, який дозволяє зберігати потрібні дані. Для
            утворення зв’язків між таблицями необхідно визначити для кожної
            таблиці первинний ключ, який являє собою одне або декілька полів, що
            однозначно ідентифікують кожен запис таблиці. -Після даного етапу
            необхідно остаточно проаналізувати утворену структуру і зв’язки між
            таблицями і усунути існуючі недоліки. Для цього вводиться необхідний
            для перевірки структури набір даних і створюються запити, які
            показують чи вдається отримати необхідні відомості. Якщо структура
            таблиць відповідає поставленим вимогам, то вводяться дані і
            створюються всі інші об’єкти – форми, запити, макроси, модулі,
            звіти.
          </li>
        </ul>
      </div>
